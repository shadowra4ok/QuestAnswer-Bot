DATA_PATH = os.path.join(os.path.dirname(__file__), "data", "qa_base.json")


def lemmatize(text: str, use_synonyms: bool = False) -> str:
    """Лемматизация + опциональная замена синонимов."""
    words = text.lower().split()
    lemmas = []
    for w in words:
        clean = w.strip("?!.,;:()[]{}\"'—–-»«")
        if not clean or clean in STOP_WORDS:
            continue

        # Проверяем синоним ДО лемматизации (для сленга)
        if use_synonyms and clean in SYNONYMS:
            lemmas.append(SYNONYMS[clean])
            continue

        parsed = morph.parse(clean)[0]
        lemma = parsed.normal_form

        if lemma in STOP_WORDS:
            continue

        # Проверяем синоним ПОСЛЕ лемматизации
        if use_synonyms and lemma in SYNONYMS:
            lemmas.append(SYNONYMS[lemma])
        else:
            lemmas.append(lemma)

    return " ".join(lemmas)


def load_knowledge_base(path: str = DATA_PATH) -> dict:
    """Загружает JSON-базу вопросов и ответов."""
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)
